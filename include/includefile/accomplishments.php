 			  	<h3>Highlights of Recent Major Accomplishments. </h3>
 			  	<h4>EDUCATION PROGRAM</h4>
 			  	<p>A program to improve education among the Muslim youth in India.
Every year the Association of Indian Muslims of America (AIM), collects donations in US and distributes them among Muslim educational institutions and students throughout India. Financial help is in the form of grants to institutions and scholarships to students.</p>
			  <strong>A few of the Muslim educational organizations that AIM has provided financial help in recent years are: </strong>
			  <ul>
			  	<li><i class="uk-icon-dot-circle-o"></i>Falah-e-Darain Education Society, Ahmedabad;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Sir Syed Institute of Management and Information Technology, New Delhi;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Raabta Education Committee, Aligarh;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Imaarat-e-Sharia, Patna;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Markaz Adab & Science, Ranchi;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Islamia Schools, Sheikhpura, Bihar;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Students Islamic Trust, New Delhi;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Faiz-e-Aaam Schools, Faizabad, UP;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Sir Syed Elementary School, Dehradun;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Anjuman-e-Islam, Mumbai;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Muslim Service Society, Calicut;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Muslim Education Society, Chennai;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Islamia Schools, Kumta, Karnatak;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Students Islamic Trust, Chennai;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>The Muslim Majlis, Port Nova, Tamil Nadu;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Taameer-e-Millat, Hyderabad;</li>
			  	<li><i class="uk-icon-dot-circle-o"></i>Al-Ameen Education Society, Mysore;</li>
			  </ul>
              <strong>A few notable highlights of this program are:</strong>
               <ul>
               	<li>1987: The AIM Education Fund, established to help needy Muslims in India.</li>
               	<li>1989: Annual distribution of AIM funds to Muslim educational institutions in India increases beyond $10,000.</li>
               	<li>1996: Established a polytechnic for poor Muslim women in Delhi, to provide them vocational training.</li>
               	<li>1998: Organized a seminar entitled “Educational Development of Muslims in India”, addressed by Mr. Saiyid Hamid, Chancellor, Hamdard University, New Delhi, India, and Dr Ishaq Jamkhanwalla, President, Anjuman-e-Islam, Bombay, India.</li>
               	<li>2004: Established the A.I.M. Industrial Training Institute (ITI) in Ahmedabad for poor Muslim youth.</li>
               	<li>2005: Established a gold medal at the Aligarh Muslim University to be awarded every year to the best student in the department of Journalism & Mass Communication.</li>
               </ul>
    










    
    
    
    
    
    
    
    
    
    
    
    
    
    

